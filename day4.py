# Part 2: Find X-MAS patterns (two MAS in X shape)
def find_xmas_pattern():
    grid = word_soup.strip().split('\n')
    
    count = 0
    rows = len(grid)
    columns = len(grid[0])
    
    # Check every position that could be the center of an X
    # We need at least 1 space around the center, so start at 1 and end at rows-1
    for row in range(1, rows - 1):
        for col in range(1, columns - 1):
            
            # The center must be 'A'
            if grid[row][col] != 'A':
                continue
            
            # Get the four corners around the 'A'
            top_left = grid[row-1][col-1]
            top_right = grid[row-1][col+1]
            bottom_left = grid[row+1][col-1]
            bottom_right = grid[row+1][col+1]
            
            # Make the two diagonal words
            diagonal1 = top_left + 'A' + bottom_right  # top-left to bottom-right
            diagonal2 = top_right + 'A' + bottom_left  # top-right to bottom-left
            
            # Both diagonals must be either "MAS" or "SAM"
            if (diagonal1 == "MAS" or diagonal1 == "SAM") and (diagonal2 == "MAS" or diagonal2 == "SAM"):
                count += 1
    
    return count

